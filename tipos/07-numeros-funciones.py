# math es un codigo escrito en python para hacer operaciones matematicas
import math

# round
# Devuelve el numero al cual se encuentre mas cercano en este caso es 1
print(round(1.3))
print(round(1.7))
print(round(1.5))

# abs
print(abs(-77))  # Nos devuelve el valor absoluto
print(abs(55))

# math
print(math.ceil(1.1))
print(math.floor(1.9999))
print(math.isnan(23))
# print(math.isnan("23")) isnan solo sirve si le pasamos numeros reales
print(math.pow(10, 3))  # Nos permite elevar un numero a una potencia
print(math.sqrt(9))  # Saca la raiz cuadrada del numero que nosotros le pasemos
