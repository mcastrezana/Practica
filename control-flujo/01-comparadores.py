# Operadores Logicos

print(1 > 2)  # False
print(1 < 2)  # True
print(1 >= 2)  # False
print(1 <= 2)  # True
print(2 >= 2)  # True
print(2 <= 2)  # True
print(2 == 2)  # True. Se debe de utilizar el doble =.
print(2 == 3)  # False
print(2 == "2")  # False
print(2 != "2")  # True. != significa no es igual a
print(2 != 2)  # False
